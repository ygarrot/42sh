#include "sh.h"

int		ft_mult(int a, int b)
{
	return (a * b);
}

int		ft_summ(int a, int b)
{
	return (a + b);
}

int		ft_div(int a, int b)
{
	return (a / b);
}

int		ft_sub(int a, int b)
{
	return (a - b);
}

int		ft_mod(int a, int b)
{
	return (a % b);
}
